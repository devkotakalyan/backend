const express = require("express");
const app = express();

app.get("/", (req, res) => {
  res.send("Backend is alive");
});

app.listen(3000, () => {
  console.log("Server running on port 3000");
});

app.use(express.json());

app.get("/users", (req, res) => {
    res.json([{ id: 1, name: "Alice" }]);
});
